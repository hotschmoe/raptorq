/// Find the systematic index entry for a given K' value.
pub fn findSystematicIndex(k_prime: u32) ?SystematicIndices {
    for (TABLE_2) |entry| {
        if (entry.k_prime == k_prime) return entry;
    }
    return null;
}

/// Round K up to the nearest K' value in Table 2.
pub fn ceilKPrime(k: u32) u32 {
    for (TABLE_2) |entry| {
        if (entry.k_prime >= k) return entry.k_prime;
    }
    return TABLE_2[TABLE_2.len - 1].k_prime;
}

/// Compute L = K' + S + H (number of intermediate symbols).
pub fn numIntermediateSymbols(k_prime: u32) u32 {
    const si = findSystematicIndex(k_prime).?;
    return si.k_prime + si.s + si.h;
}

/// Compute the number of LT symbols: W from Table 2.
pub fn numLTSymbols(k_prime: u32) u32 {
    const si = findSystematicIndex(k_prime).?;
    return si.w;
}

/// Compute the number of PI symbols: P1 = smallest prime >= P, where P = L - W.
pub fn numPISymbols(k_prime: u32) u32 {
    const si = findSystematicIndex(k_prime).?;
    const l = k_prime + si.s + si.h;
    return helpers.nextPrime(l - si.w);
}
